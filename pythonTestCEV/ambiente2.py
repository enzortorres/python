def somar(a = 0, b = 0, c = 0):
    """
    -> Faz a soma de três valores e mostra o resultado na tela
    :param a: Primeiro valor
    :param b: Segundo valor
    :param c: Terceiro valor
    Função criada por Enzo Ribas Torres.
    """
    s = a + b + c
    print(f'A soma vale {s}.')

somar(3, 2, 5)
somar(8, 4)
somar(3)
somar()